int max_bit;
int dfs(int now,int visited_bit,vector<vi>& edge){
    visited_bit += 1<<now;
    if (visited_bit==max_bit) return 1;
    int res=0;  
    for(auto to:edge[now]){
        if(!(visited_bit&(1<<to))) res += dfs(to,visited_bit,edge);
    }
    return res;
}
