    for i,s in enumerate(S):
        if(s=="o"):
            mask |= 1 << i 

    N=len(S)
    ans = inf
    judge_mask = (1<<N)-1
    for bit in range(1<<N):
        ok = 0
        for i in range(N):
            if(bit>>i&1):
                ok |= mask << i
        ok |= ok >> N

        if(judge_mask == judge_mask & ok):
            ans = min(ans, bin(bit).count("1"))

