def ST(): return input().replace('\n', '')
def main():
    N,M = MI()
    S=ST()
    ans_rev = []
    S_rev = S[::-1]
    i=0
    while i<N:
        step = False
        for j in range(1,M+1)[::-1]:
            if i+j>N:
                continue
            if S_rev[i+j]=="0":
                step=True
                ans_rev.append(j)
                i += j
                break
        if not step:
            print(-1)
            exit()
    print(*ans_rev[::-1])
