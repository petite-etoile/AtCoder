vector<tuple<int,int,int,int>> query;
int N,M;
int f(vector<int>& A){
    int res = 0;
    for(auto e:query){
        int a,b,c,d; tie(a,b,c,d) = e;
        if(A[b]-A[a]==c) res += d;
    }

    return res;
}
int dfs(int now, int before, vector<int>& A){
    if(N==now) return f(A);

    int res = 0;
    for(int x=before; x<=M;x++){
        A[now] = x;
        int tmp = dfs(now+1,x,A);
        chmax(res, tmp);
    }
    return res;
}

