def main():
    #BIT DP
    N,M=MI()
    max_bit = 1<<N
    DP = [inf]*max_bit
    DP[0]=0
    SC = []
    for i in range(M):
        S,C = input().split()
        C=int(C)
        bit = 0
        for i,s in enumerate(S):
            if s=="Y":
                bit+=1<<i
        SC.append((bit,C))
        
    for bit,c in SC:
        for mask in range(max_bit):
            DP[bit|mask]=min(DP[bit|mask], DP[mask]+c)
    
    if DP[-1]==inf:
        print(-1)
    else:
        print(DP[-1])


if __name__ == '__main__':
    main()