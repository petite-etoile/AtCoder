def ST(): return input().replace('\n', '')
def main():
    N = I()
    A = LIN(N)[::-1] #bisect使いたいので
    LIS = [A[0]] #もとの順序の広義単調減少は反転させた配列の広義単調増加
    for a in A[1:]:
        if LIS[-1] <= a:
            LIS.append(a)
        else:
            update_index = bisect_right(LIS,a)
            LIS[update_index] = a
    print(len(LIS))

if __name__ == '__main__':
    main()