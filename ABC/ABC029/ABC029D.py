    dp[0][0][0] = 1
    for i in range(len(S)):
        for j in range(len(S)):
            for less in range(2):
                if less:
                    upper = 9
                else:
                    upper = int(S[i])
                for k in range(upper+1):
                    dp[i+1][j+(k==1)][less or k<int(S[i])] += dp[i][j][less]
    ans = 0
    for one in range(1,len(S)+1):
        ans += one * sum(dp[-1][one])
    print(ans)
