def LLI(): return [list(map(int, l.split() )) for l in input()]
def I(): return int(input())
def F(): return float(input())
def ST(): return input().replace('\n', '')
def main():
    #####部分点##### n(n-1)/2回
    N=I()
    ans = 0
    max_dif = 0
    v = 0
    #任意の点(今回は0)から一番遠いvを見つける
    for i in range(1,N):
        print("?",1,i+1,flush=True)
        d = int(input())
        if d>max_dif:
            max_dif=d
            v=i+1
    
    for i in range(1,N+1):
        if i==v:
            continue
        print("?",i,v,flush=True)
        d = int(input())
        ans = max(ans, d)
    print("!",ans)
        

if __name__ == '__main__':
    main()
